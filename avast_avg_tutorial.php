<div class="principal">

<h1>Configuração do Antivírus</h1>

<div style="margin-bottom:20px">Muitas vezes o antivírus pode bloquear seu acesso ao site da Receita Federal (E-CAC) e ao Conectividade Social, necesários ao cumprimento de suas obrigações fiscais. Para evitar este problema, você pode incluí-los na lista de endereços confiáveis. Este procedimento só é necessário se, mesmo após configurar o Java e o Internet Explorer, ainda não tiver sucesso em acessar esses sites.<br>
<br>
Mostramos a seguir como incluir o <strong>E-CAC</strong> e o <strong>Conectividade Social</strong> na lista de exceções dos antivirus AVG e AVAST, que são os mais comumente usados. Se você utiliza outro antivírus, os procedimentos são semelhantes.</div>

<h2>AVAST</h2>

<ol>
<li>Abra o painel do Avast e clique na aba “Configurações”</li>
<li>Clique na opção “Antivírus”</li>
<li>Role a página até achar a opção “Exclusões”</li>
<li>Vá na aba URL e digite os endereços: <strong>https://conectividade.caixa.gov.br</strong> e <strong>https://cav.receita.fazenda.gov.br</strong></li>
<li>Clique em “OK”</li>
</ol>
<br>
<h2>AVG</h2>

<ol>
<li>No menu Opções selecione Configurações avançadas</li>
<li>No painel esquerdo, selecione Exceções</li>
<li>No painel direito, clique em Adicionar exceção</li>
<li>No menu suspenso, em tipo de exceção, selecione URL</li>
<li>Inclua o endereços: <strong>https://conectividade.caixa.gov.br</strong> e <strong>https://cav.receita.fazenda.gov.br</strong></li>
<li>Clique em OK para salvar os detalhes dessa exceção</li>
<li>Clique em OK novamente para salvar as configurações</li>
</ol>

</div>

