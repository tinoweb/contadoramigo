<h2> Transmissão da DIRF 2017</h2>
  
<div style="margin-bottom:20px">
  <p>Você só precisa enviar a <strong>DIRF</strong> se a sua empresa fez no último ano pelo menos uma  retenção de IR nos pagamentos de pró-labore,  remuneração a trabalhadores autônomos, pessoas jurídicas, ou no pagamento do aluguel. Neste documento você informará à Receita Federal os valores de cada uma das retenções realizadas e para quem foram feitas. Para preencher a DIRF, faça o download e instale o <a href="http://idg.receita.fazenda.gov.br/orientacao/tributaria/declaracoes-e-demonstrativos/dirf-declaracao-do-imposto-de-renda-retido-na-fonte/tabelas-pgds/programa-gerador-da-declaracao-dirf-2018" target="_blank">programa gerador da DIRF 2018</a> (selecione a versão 32 bits, pois a de 64 bits é incompatível com o Java). Baixe e instale também o <a href="http://www.receita.fazenda.gov.br/publico/programas/receitanet/Receitanet-1.10.exe" target="_blank">Receitanet</a> (usado para transmitir o documento).<br />
    <br />
    Se você já enviou a DIRF<strong>, </strong>mas precisa corrigi-la, veja como <a href="dirf_retificacao.php">fazer a retificação da Dirf</a>. <br />
    <br />
    Caso esteja enviando a DIRF pela primeira vez, certifique-se de  ter cadastrado no Contador Amigo todos os pagamentos referentes a pró-labore, salários, trabalhadores autônomos e a pessoas jurídicas prestadoras de serviços, efetuados no ano de 2017. <br />
    <br />
    Feito isto, <a href="dirf_gerar.php">baixe aqui o relatório de retenções</a>. Este arquivo contém todos os valores retidos de IR dos pagamentos cadastrados por você no Contador Amigo.<br>
<br>
Em seguida, proceda como indicado a seguir.<br />
