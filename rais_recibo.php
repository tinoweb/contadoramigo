<h2>RAIS - Recibos de entrega</h2>
É possível obter os <strong>recibos de entrega da RAIS</strong> diretamente na página de <a href="http://www.rais.gov.br/sitio/recibo_identificacao.jsf" target="_blank">Impressão de Recibo</a> do site da RAIS na Internet. Para isso você precisa do número do CREA (Controle de Recepção de Arquivo).  Este número pode ser encontrado no protocolo de envio. É gerado automaticamente no momento em que a declaração é enviada. Se você não tiver o número do CREA, poderá solicitá-lo da seguinte forma:<br />
<br />
<ul> 
<li><strong>Obtenção de CREA para RAIS ano-base a partir de 2008 </strong><br />
  Acesse a <a href="http://www.rais.gov.br/sitio/consulta_crea_identificacao.jsf" target="_blank">página de Consulta</a> do site da RAIS, preencha o formulário e obtenha o número do CREA na hora. Você precisará porém, informar o nome e CPF do responsável pelo preenchimento  da declaração na época (exatamente como digitado na RAIS). Se você  não souber, deverá obter o número do CREA, preenchendo o <a href="https://cssinter.serpro.gov.br/SCCDPortalWEB/pages/dynamicPortal.jsf?ITEMNUM=2024" target="_blank">formulário de solicitação</a> do Serpro. Neste formulário será pedido o CPF do responsável pelo envio da <strong>última RAIS</strong>. A resposta será enviada por e-mail. Se você também não souber o CPF do responsável pela última declaração, não se desespere. Você poderá obter o recibo da declaração desejada enviando um pedido para o Ministério do Trabalho. Para mais orientações nesse sentido, entre em contato com o número 158.</li>
</ul>


