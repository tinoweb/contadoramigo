<div class="titulo" style="margin-bottom:20px;"><h1>Abertura de empresa</h1></div>

    <h2>CCM (Inscricão no município)</h2>
    
    Acesse o <a href="https://ccm.prefeitura.sp.gov.br/login/contribuinte?tipo=I" target="_blank">Requerimento de Inscrição no CCM (Pessoa Jurídica)</a> e preencha o formulário conforme instruções abaixo:
      
      <ul>
      <li><b>Nome empresarial:</b> o nome completo com o qual registrou sua empresa.<br>
        <br>
      </li>
      <li><strong>Data de Início de funcionamento:</strong> a data que consta no seu <a href="http://www.receita.fazenda.gov.br/pessoajuridica/cnpj/cnpjreva/cnpjreva_solicitacao.asp" target="_blank">cartão CNPJ</a>.<br>
        <br>
      </li>
      <li><strong>Órgão de Registro:</strong> informe onde registrou sua empresa, junta ou cartorio. Sociedades de advogados devem selecionar &quot;outros&quot;, já que o registro é feito na OAB.<br>
        <br>
      </li>
      <li><b>Endereço do estabelecimento:</b> informe o número de contribuinte do IPTU, que pode ser encontrato em qualquer recibo de pagamento do IPTU e o CEP da empresa. Os demais campos do endereço serão preenchidos automaticamente.<br>
        <br>
      </li>
      <li><b>O contribuinte enquadra-se como Sociedade de Profissionais - SUP?</b><br>
        Se você for um profissional liberal (médico, advogado, engenheiro, dentista, etc) e estiver montando uma empresa sozinho, ou com outros colegas da mesma profissão, responda SIM. Caso contrário, responda NÃO.<br>
        <strong><br>
        </strong></li>
      <li><strong>Tipo de Unidade</strong>: selecione &quot;Produtiva&quot;.
        <br>
        <br>
      </li>
      <li><b>CNAE: </b>informe o(s) código(s) CNAE cadastrado(s) em seu cartão CNPJ, Identificado o principal e os secundários.<br>
        <br>
      </li>
      <li><strong>Código de Serviço:</strong> Com base no(s) CNAE informado(s), são exibidos os códigos de serviços correspondentes. Selecione aquele que melhor descreve sua atividade.<br>
        <br>
      </li>
      <li><b>Contribuinte gera resíduos sólidos de saúde?  </b><br>
        São exemplo de establecimento gerador de  resíduos sólidos de saúde, os hospitais, farmácias, clínicas médicas, odontológicas e veterinárias, centros de saúde, laboratórios, ambulatórios, centros de zoonoses, prontos-socorros e casas de saúde.<br>
        <br>
      </li>
      <li>  <b>Contribuinte utiliza anúncios?</b><br>
        Refere-se a placas ou cartazes na fachada de seu establecimento. Ao responder SIM a esta pergunta, aparecerá uma  lista de códigos, para cobrança anual da
        <a href="http://www.prefeitura.sp.gov.br/cidade/secretarias/financas/servicos/taxas/index.php?p=2529" target="_blank">TFA - Taxa de Fiscalização de Anúncios</a>.<br>
        <br>
      </li>
      <li><strong>Sócios, titulares ou diretores:</strong> nome e   CPF do proprietário, sócios e administradores responsáveis pela pessoa jurídica conforme informado no cartão CNPJ.<br>
        <br>
      </li>
      <li><strong>Responsável pela declaração</strong>: nome e  CPF de quem está se responsabilizando, administrativa, civil e penalmente, pelas informações prestadas neste formulário eletrônico.<br>
        <br>
      </li>
      <li><strong>Instituição Finananceira:</strong> se você não é uma instituição financeira (bancos e assemelhados), marque não.<br>
      </li>
      </ul>
    Preenchido formulário, clique no botão <b>Enviar</b>, imprima o <b>Protocolo</b> e leve-o, acompanhado da <b>documentação exigida</b>, até a 
 Praça de Atendimento da Secretaria Municipal de Finanças e Desenvolvimento Econômico (localizada no <a href="https://www.google.it/maps/place/Pra%C3%A7a+de+Atendimento+Prefeitura+de+S%C3%A3o+Paulo/@-23.5467725,-46.6390999,17z/data=!3m1!4b1!4m2!3m1!1s0x94ce58537be69655:0xd641d750fea5da96" target="_blank">Vale do Anhangabaú, 206</a>), ou em alguma Sub-Prefeitura. Na Praça de Atendimento da Secretaria de Finanças, é preciso fazer o <a href="http://agendamentosf.prefeitura.sp.gov.br/forms/BemVindo.aspx" target="_blank">agendamento prévio da visita</a>.<br>
<br>
Você já sairá de lá com a sua <b>Senha Web</b>, que lhe datá acesso ao site da Nota Fiscal Eletrônica, onde você poderá imprimir suas notas fiscais. <br>
<br>
Na ocasião, a Prefeitura lhe entregará também  a(s) guia(s) para você recolher a TFE e a TFA (se houver anúncio). O  recolhimento da(s) taxa(s) deve ser feito até o dia 10  do segundo mês da abertura. A partir do ano seguinte a cobrança chegará automaticamente pelo Correio no endereço de sua empresa, no mês de julho. <br>
