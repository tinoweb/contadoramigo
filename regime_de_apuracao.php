Todo ano você precisa informar à Receita Federal qual o regime de apuração de receitasa ser adotado por sua empresa. Há duas opções: o <strong>regime de Caixa</strong>, em que seus impostos são calculados com base nos valores efetivamente recebidos, e o <strong>regime de Competência</strong>, no qual os impostos são calculados com base nas notas emidas. À primeira vista pode parecer que o regime de Caixa é mais interessante, pois você só pagaria impostos sobre aquilo que efetivamente &quot;entrou no caixa&quot; da empresa. Esta opção, porém, exige que você faça a escrituração fiscal completa, com demonstrativos e balanços. Dessa forma, recomendamos usar sempre o regime de Competência. É simples, claro e sem complicação. Veja a seguir como fazer a opção:<br />
