<h2 style="margin-left:60px; font-size: 16px; margin-bottom: 30px">Certidão Negativa de Débitos Estaduais</h2>

A  Certidão Negativa de Débitos Estaduais indica que você está em dia com suas obrigações perante o Estado. É geralmente fácil de obter já que em raríssimas situações prestadores de serviços estão sujeitos aos tributos estaduais, como o ICMS. Se você possui veículos em nome da empresa e não estiver em dia com o IPVA, pode ter problemas em obter esta certidão. Em muitos estados brasileiros este certificado já pode ser obtido pela internet nos sites das respectivas juntas comerciais. No <strong>Estado de São Paulo</strong>, a certidão negativa pode ser obtida <a href="https://www10.fazenda.sp.gov.br/CertidaoNegativaDeb/Pages/EmissaoCertidaoNegativa.aspx">aqui</a>.<br />
  <br />
Caso seu Estado não emita este tipo de certificado pela internet, você precisará se dirigir pessoalmente à Junta Comercial  se quiser emitir o documento. Em caso de pendência, você será informado e poderá quitar todos os deus débitos.<br />
<br />
<strong>Divida comum x Dívida Ativa</strong><br />
Se você possuir débitos muito antigos com o Estado, é possível que eles tenham sido encaminhados para a <strong>Divida Ativa</strong>. Isto significa que  está a um passo de vir a ser cobrada judicialmente, podendo inclusive implicar no leilão de bens em nome da empresa. Dessa forma, fique atento, você pode pensar que não débito nenhum, porque ele foi retirado da cobrança comum e  está na dívida ativa. De qualquer forma, a Certidão Negativa só será emitida se você não possuir débitos de nenhuma espécie.<br />
<br />
<br />
<strong>Parcelamento e anistia</strong><br />
Se o seu débito for muito grande, sempre é possível solicitar o parcelamento. Fique atento também à anistia. Periodicamente o governo decreta a redução dos juros de algumas dívidas dos contribuintes para ajudá-los na regularização de seu débitos. Isso ocorre com mais frequência no início do ano.

