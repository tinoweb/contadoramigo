  <h1>Impostos e Obrigações</h1>
  <h2>DIMOB</h2>
	A Declaração de Informações sobre Atividades Imobiliárias – Dimob é de entrega obrigatória <strong>exclusivamente para empresas que:</strong><br>
<ul>
	<li>comercializarem imóveis</li>
	<li>intermediarem aquisição, alienação ou aluguel de imóveis</li>
	<li>fazem sublocação de imóveis</li>
	<li>realizam a construção, administração, locação ou alienação do patrimônio próprio, de seus condôminos ou sócios.
	</li>
	</ul>
Se a sua empresa não realizou operações imobiliárias no ano-calendário de referência não precisa enviar a DMOB.<br>
<br>

Na declaração, serão apresentadas  as informações relativas a todos os imóveis comercializados e/ou locados, ainda que tenha havido a intermediação de terceiros. A Declaração deve ser enviada a cada ano, até o último dia útil do mês de fevereiro. Para tal, você deve dispor de um certificado digital tipo E-CNPJ e baixar 2 programas:<br>
	<ul>
	<li><a href="http://idg.receita.fazenda.gov.br/orientacao/tributaria/declaracoes-e-demonstrativos/dimob-declaracao-de-informacoes-s-atividades-imobiliarias/programa-gerador-da-dimob-pgd" target="_blank">DIMOB 2017</a> (para o preenchimento da declaração) </li>
	<li><a href="http://idg.receita.fazenda.gov.br/programas-para-download/receitanet/download-do-programa-receitanet" target="_blank">Receita Net</a> (para transmitir sua declaração pela Internet)</li>
	</ul>
Os programas são válidos também para entrega de declarações em atraso, desde 2010.<br>
<br>

Em caso de dúvida no preenchimento de sua declaração, contate nosso <a href="suporte.php">Help Desk</a>

