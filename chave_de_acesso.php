  <h1>Impostos e Obrigações</h1>
  <h2>Obtenção da chave de acesso gratuita ao Conectividade Social</h2>

<div>

Se você, exercendo seu direito, não quer pagar um certificado, a Caixa criará uma chave de acesso gratuitamente para você! Parece legal, não é? Então prepare-se. Para obtê-la, você deve primeiro baixar e instalar em seu micro o <a href="http://www.contadoramigo.com.br/downloads/precert_cns_1_0_0_37.exe">Cliente de Certificação</a> (nem pense em usar um Mac, tem que ser  PC). Providencie também um <em>pendrive </em>formatado. No Aplicativo de Certificação você fará 3 coisas: <strong>cadastrar os dados da empresa</strong>, gerar uma <strong>chave provisória</strong>, que deverá ser gravada em um <em>pendrive </em>e imprimir o <strong>Termo de Adesão</strong> (em duas vias) <br />
 <br />
 O próprio aplicativo o orienta  a realizar tais tarefas, mas se tiver alguma dificuldade, veja  nosso <a href="cliente_de_certificacao.php">tutorial do Cliente de Certificação</a> ou envie uma mensagem ao  <a href="https://contadoramigo.websiteseguro.com/suporte.php">Help Desk</a>. Feito isto, você deverá dirigir-se a uma das agências credenciadas da Caixa para apresentar a documentação e homologar a chave<em></em>. Deixe claro que está falando da chave PRI gratuita e não do certificado digital, pois a Caixa vende o certificado também. Procure se informar qual  agência  está habilitada a realizar este serviço em sua cidade. Em São Paulo, há apenas duas:
<ul>
<li>Agência Praça Da Árvore (Avenida Jabaquara, 442)</li>
<li>Agência Carlos Sampaio (Avenida Paulista, 306 - em frente à estação Brigadeiro do Metrô)</li>
</ul>

Você deverá levar os seguintes documentos:
<ul>
<li>Termo de Adesão (aquele que você imprimiu no Aplicativo de Certificação)</li>
<li><em>Pendrive</em> com a chave provisória gravada nele</li>
<li>Contrato Social da Empresa (original e cópia)</li>
<li>Comprovante do CNPJ. Imprima-o <a href="http://www.receita.fazenda.gov.br/pessoajuridica/cnpj/cnpjreva/cnpjreva_solicitacao.asp" target="_blank">aqui</a> (original e cópia)</li>
<li>Carteira de identidade ou de habilitação com foto (original e cópia)</li>
</ul>

Retornando da agência com <em>pendrive</em> validado, acesse novamente o <strong>Cliente de Certificação</strong> para gravação a chave definitiva, conhecida como &quot;Chave PRI&quot;. Siga nosso <a href="gravacao_chave_pri.php">tutorial de gravação da Chave PRI</a>.<br />
<br />
Se conseguiu realizar todos os procedimentos, você venceu mais uma vez o &quot;sistema&quot; e poderá enviar a Gfip, sem ter que pagar por um certificado!<br />
<br />

O processo de geração da Gfip em si é o mesmo utilizado por aqueles que possuem o Certificado Digital pago (ICP), mas a transmissão do arquivo  para a Receita Federal<strong> não se dá pelo pelo site Conectividade Social da Caixa</strong> e sim por meio de um outro software que você precisa baixar e instalar em seu micro: o <a href="downloads/conectividade_social_instalacao_v1207.EXE">CNS - Conectividade Social</a>.  <br />
<br />
Depois de instalado, clique com o botão direto do mouse no ícone do programa (em sua área de trabalho), vá em <strong>Propriedades</strong>, selecione a aba <strong>Compatibilidade</strong> e marque a opção <strong>Executar este programa como administrador</strong>, caso contrário ele não rodará. Em seguida, abra o programa e  faça a <a href="conectividade_cns_configuracao.php">configuração inicial</a>.<br />
<br />
Pronto, agora você está apto a gerar as guias de seus impostos e enviar declarações, sem ter que pagar por um certificado digital! <br />
