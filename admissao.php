<h3>3. Agendar o exame admissional</h3>
	
O funcionário não pode iniciar na empresa sem que tenha obtido um atestado médico liberando-o para a atividade que irá desempenhar. Para isso, você deve agendar o exame admissional. O custo desse exame corre por conta do empregador, mas é relativamente baixo. Procure em sua cidade clínicas credenciadas para a realização de exames admissionais e agende a visita de seu futuro colaborador.<br><br>
<br>


<h3>4. Registrar no Livro de de Funcionários</h3>

Preencha os dados do funcionário e cole a foto dele no livro de registro de funconários. Se você aidna não possui este livro, poderá comprar um na papelaria. Há também softwares para registro de funcionários, mas normalmente para pequenas empresas com poucos funcionários não vale a pena adquiri-los.<br><br>
<br>


<h3>5. Redigir o contrato de trabalho</h3>
	
Ao admitir um funcionário, você deve fazer o contráto de trabalho. As cláusulas desste contrato deverão prever o <strong>período de experiência</strong>, que ser de até <strong>90 dias</strong>. Este período serve para você avaliar o desempenho do funcionário. Caso não o julgue adequado para a função, poderá dispensá-lo sem aviso prévio e sem a multa do FGTS. Deverá, porém, sempre pagar o 13º e as férias proporcionais.<br><br>

Terminado o período de experiência, o contrato passa a velor por prazo indeterminado.  O funcionário terá direito à indenização em caso de dispensa sem justa causa e à todas as garantias previstas na legislação.<br><br>

Para facilitar seu trabalho preparamos um <a href="downloads/contrato_de_trabalho.docx" target="_blank">modelo básico de um contrato de trabalho</a>.<br><br>
<br>


