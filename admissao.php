<h2>Admissão de Funcionários</h2>
 
Nesta página você conhecerá cada uma das etapas necessárias à contratação de funcionários. As regras valem para qualquer tipo de empresa, inclusive para o MEI. Veja como proceder:<br><br>

  
<h3>1. Consultar a convenção coletiva da categoria</h3>
Antes de estabelecer o salário do novo funcionário e os benefícios que ele terá, é muito importante consultar a <strong>convenção coletiva de trabalho</strong> aprovada entre o sindicato da categoria do trabalhador e o sindicato da categoria de sua empresa. Mesmo que ele ou sua empresa não sejam associados a nenhum sindicato, as convenções coletivas têm força de lei e valem para todos os funcionários da categoria, sindicalizados ou não.<br><br>

Nas convenções coletivas são determinados os pisos salariais, a jornada máxima de trabalho, os benefícios obrigatórios, entre outras questões. Normalmente o texto destas convenções pode ser encontrado facilmente na Internet. Fique atento à data da convenção, ao estado e às categorias envolvidas, para não se enganar. <br><br>

Normalmente a convenção é repactuada a cada ano, após longas negociações entre o sindicato e as grandes empresas do setor. Assim, você deverá anualmente consultar os percentuais de reajuste salariais aprovados e eventuais alterações nas cláusulas contratuais, para manter seu funcionário sempre dentro das normas estebelecidas.<br><br><br>


