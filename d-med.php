  <h1>Impostos e Obrigações</h1>
  <h2>D-MED</h2>
A Declaração de Serviços Médicos e de Saúde – Dmed é de entrega obrigatória <strong>exclusivamente para empresas da área da saúde</strong>, tais como psicólogos, fisioterapeutas, terapeutas ocupacionais, fonoaudiólogos, dentistas, hospitais, laboratórios, serviços radiológicos, serviços de próteses ortopédicas e dentárias, e clínicas médicas de qualquer especialidade.<br>
<br>
Devem ser informados os valores recebidos de <strong>pessoas físicas</strong>, em decorrência de pagamento pela prestação de serviços médicos e de saúde, e plano privado de assistência à saúde.<br>
<br>
Não devem ser informados na Dmed valores recebidos de pessoas jurídicas ou do Sistema Único de Saúde (SUS).<br>
<br>
A Declaração deve ser enviada a cada ano, até o último dia útil do mês de fevereiro. Para tal, você deve dispor de um certificado digital tipo E-CNPJ e baixar 2 programas:<br>
	<ul>
	<li><a href="http://idg.receita.fazenda.gov.br/orientacao/tributaria/declaracoes-e-demonstrativos/dmed-declaracao-de-servicos-medidos-e-da-saude/programa-gerador-da-dmed-2017" target="_blank">DMED 2017</a> (para o preenchimento da declaração) </li>
	<li><a href="http://idg.receita.fazenda.gov.br/programas-para-download/receitanet/download-do-programa-receitanet" target="_blank">Receita Net</a> (que possibilitá ao DMED transmitir sua declaração pela Internet)</li>
	</ul>
Os programas são válidos também para entrega de declarações em atraso, desde 2012.<br>
<br>

Em caso de dúvida no preenchimento de sua declaração, contate nosso <a href="suporte.php">Help Desk</a>

