<h1>Impostos e Obrigações</h1>


  <h2>Contribuição Sindical</h2>
  <p> Segundo a portaria nº 10 de 06.01.2011 do Ministério do Trabalho, a<strong> Contribuição Sindical Patronal  é facultativa para empresas optantes pelo Simples</strong>. Ela pode, porém, ser útil para prestar auxílio jurídico e para os processos de contratação e demissão de funcionários. Se você optar por pagá-la, deve escolher um sindicato que se adeque a sua atividade e solicitar o cadastramento. As contribuições são pagas em janeiro (ou no momento em que sua empresa é filiada) e têm um custo relativamente baixo, proporcional ao número de funcionários.</p>
  <br>
A Contribuição Sindical Patronal não deve ser confundida com a<strong> Contribuição Associativa</strong>, que é paga mensalmente e é atribuída somente aqueles que optaram por tornarem-se sócios ativos 
  no sindicato, com direito a voto e participação em assembleias. Estes sócios também devem arcar com mais duas contribuições:
<ul>
  <li><strong>Contribuição Confederativa</strong>, geralmente cobrada em agosto, e  destinada a financiar as confederações de sindicatos nos diferentes estados.</li>
  <li><strong>Contribuição Assistencial</strong>, cobrada por alguns sindicatos que por participarem de negociações coletivas têm despesas adicionais e precisam rateá-las com os associados</li>
  </ul>

