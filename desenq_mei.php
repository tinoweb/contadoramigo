	  <div class="tituloVermelho" style="margin-bottom: 10px">Primeira entrada na Junta</div>
É gratuita e trata-se de uma simples comunicação de desenquadramento. Leve os seguintes documentos à unidade da Junta Comercial de sua cidade: 
    <ul>
      <li><a href="http://www8.receita.fazenda.gov.br/SIMPLESNACIONAL/aplicacoes.aspx?id=22" target="_blank">Comunicação de desenquadramento</a></li>
      
      <li><a href="downloads/processos_especificos.pdf" target="_blank">Formulário  de desenquadramento</a> (em &quot;opção de alteração&quot; escolha  &quot;Outros&quot; e em &quot;Atos&quot; escreva &quot;desenquadramento de SIMEI&quot;)</li>
      
		<li><a href="downloads/requerimento_de_empresario.docx">Requerimento de empresário (Simplificado)</a>.</li>
    </ul>

   Para saber se o requerimento foi aprovado, acesse <strong>somente com o Internet Explorer</strong> a <a href="https://www.jucesp.sp.gov.br/vre/Consulta/ConsultaAndamento.aspx" target="_blank">página de Consulta de Processos</a> do Via Rápida Empresa. Se o site aparecer desconfigurado, vá nas configurações do Internet Explorer, entre em Configurações do Modo de Exibição de Compatibilidade e inclua o site <em>sp.gov.br</em>.<br><br>

    
    <div class="tituloVermelho" style="margin-bottom: 10px">Segunda entrada na Junta</div>
    Na segunda entrada, você deve fazer a adequação dos dados cadastrais da empresa, contemplando o novo status. As adequações necessárias são:<br>
     
     <ul>
		 <li><strong>Alteração da Razão Social e criação de um nome fantasia</strong>
			 Enquanto MEI, o nome da empresa vem seguido do seu CPF, o que, convenhamos, não é muito bonito. Agora sua razão social deverá ficar assim: SEU NOME - ME. Para seus impressos e cartazes pode estipular também um nome fantasia.</li>
     
      <li><strong>Alteração do Capital Social </strong><br />
		  Ao transformar-se em Empresário Individual, você deve estipular um capital social. O valor pode ser fixado livremente e deve ser  compatível com os bens em nome da empresa. O capital social é levado em conta pelo banco na aprovação de linhas de crédito.</li>
	<li><strong>Outros</strong><br>
	Aproveite também para informar outras alterações, como mudança de atividade, endereço e inclusão de sócio, se necessário.</li>
	  </ul>

Esta segunda entrada requer o pagamento de duas taxas, a Dare e a Darf. O valor varia de R$ 80 a R$ 200, de acordo com as alterações que você deseja fazer e se vai incluir sócios na empresa. A mudança envolve as três esferas de governo: <strong>federação</strong>, <strong>estado</strong> e <strong>município</strong>, pois cada uma possui seu próprio registro e todos precisam ser atualizados. Proceda da seguinte forma:<br>
<br>

<strong>Alteração na Receita Federal</strong><br>
Para alterar os dados de sua empresa no <strong>CNPJ</strong>, acesse nosso <a href="orientacoes_dbe.php">tutorial para geração do DBE</a>.<br><br>

<strong>Alteração no Estado</strong><br>
Este processo é todo online e utiliza o site Via Rápida Empresa. Siga nosso <a href="alteracao_contrato_junta_sp.php">tutorial de alteração na Junta Comercial de São Paulo</a>. <br><br>


<strong>Alteração na Prefeitura</strong><br>
Após a alteração na junta, as empresas <strong>prestadoras de serviços</strong> precisam atualizar também seu registro na prefeitura. Para isso você deverá preencher um requerimento de atualização. Algumas cidades já possibilitam o preenchimento online. Verifique se esta facilidade  já está disponível em seu município. Mesmo com o preenchimento online, é bastante provável que você ainda precise ir até a Prefeitura para que seus dados sejam conferidos e sua identidade confirmada.<br>
