  <h1>Como transformar o MEI em Microempresa (ME)</h1>
  <strong>A transformação do MEI (Microempreendedor Individual) em ME (Microempresa) pode ser feita a qualquer momento por opção própria do empreendedor, ou por comunicação obrigatória nos seguintes casos:</strong>
  <ul>
  <li>Faturamento bruto acima do limite anual (R$ 81 mil)<br />
    </li>
    <li>Contratação de mais de um funcionário</li>
    <li>Entrada de um sócio na empresa</li>
    <li>Abertura de filial ou outra empresa em nome do empresário</li>
    <li>Exercer novas atividades vedadas ao MEI </li>
</ul>
Se você se desenquadrar por<strong> opção própria</strong> (ou porque seu faturamento ultrapassou em até 20% o limite anual) seu pedido <strong>terá efeito a partir de 1º de janeiro do seguinte</strong>, salvo quando a comunicação for feita no mês de janeiro. Neste caso os efeitos se darão no mesmo ano.<br>
<br>
No desenquadramento por <strong>comunicação obrigatória</strong>, há duas situações: <br>
<br>
Se o seu <strong> faturamento ultrapassou em mais de 20% o limite</strong> previsto, o desequadramento <strong>terá efeito retroativo a janeiro do mesmo ano</strong>. Isso não é bom, uma vez que implicará no pagamento dos impostos devidos como se você já estivesse desenquadrado desde o início do ano, acrescidos de juros e correção. Se você está se desenquadrando  porque contratou mais de um funcionário, incluiu um novo sócio na empresa, abriu uma filial ou passou a exercer atividade vedada ao MEI, seu pedido terá efeito a partir do <strong>primeiro mês subsequente</strong>.<br>
<br>
<div class="quadro_branco" style="margin-bottom: 0px"><div class="destaque" style="margin-bottom:5px">DICA 1</div>
<div>Se  você tem pressa e não quer esperar até o ano que vem para virar ME, solicite o descredenciamento por <strong>comunicação obrigatória </strong>motivada pela inclusão de sócio (natureza jurídica vedada) ou por inclusão de atividade econômica vedada. Assim a transformação em ME se dará já no mês seguinte ao deferimento do pedido.</div></div>
  
 <div class="quadro_branco"><div class="destaque" style="margin-bottom:5px">DICA 2</div>
<div>Recomendamos que providencie um certificado digital E-CNPJ antes de solicitar o desenquadramento. Você precisará dele para fazer a adequação cadastral na Junta e  o certificado será necessário também para gerar as guias de seus impostos como ME. Assinantes do Contador Amigo podem adquiri-lo pela Valid Certificadora com um super desconto: apenas R$ 189,75, em 3 x sem juros. <a href="http://www.validcertificadora.com.br/e-CNPJ-A1.htm/388C546B-98DA-4A0E-B8F3-89BBA8FB5FEE/RD007797" target="_blank">Solicite agora mesmo o seu</a>.</div></div>
   
 
    

    <div class="tituloVermelho">1. Solicitando o desenquadramento</div>
    
<div style="margin-bottom:20px">Entre na <a href="http://www8.receita.fazenda.gov.br/SIMPLESNACIONAL/Servicos/Grupo.aspx?grp=3" target="_blank">página de serviços do SIMEI</a>, no portal do Simples Nacional, e solicite o desenquadramento. <br>

<br><br>
  <div class="tituloVermelho">2. Verificando se o desenquadramento foi aprovado</div>
  Para saber se o seu desenquadramento já foi aprovado, acesse a <a href="http://www8.receita.fazenda.gov.br/SimplesNacional/aplicacoes/ATBHE/Consultaoptantes.app/Consultaropcao.aspx" target="_blank">Consulta de Optantes do Simples Nacional</a> . Fique atento à <strong>data de efeito</strong>. Ela indica quando você passará a recolher os impostos como ME. Se a data de efeito for, por exemplo, 31/03/2017, a partir de abril você já será uma ME. Como os impostos recolhidos se referem sempre ao movimento do mês anterior, em abril você ainda recolherá a DAS como MEI, pagando aquele valor fixo de sempre. Somente em maio é que você recolherá os impostos como ME, referentes ao seu movimento de abril. 
  <br>
<br>
Para saber como recolher seus impostos como ME, <a href="assinatura.php">cadastre-se no Contador Amigo</a>, confira as obrigações do mês e siga nossos tutoriais.<br>
<br>

A última obrigação que você deverá cumprir como MEI é a entrega da DASN (Declaração Anual):
<ul>
	<li>Se estiver se desenquadrando por inclusão de sócio ou atividade impeditiva, deverá enviar a <strong>DASN Especial</strong>, referente ao ano corrente.</li> 
	<li>Se estiver desenquadrando por excesso de receita <strong>em mais de 20% </strong>não precisará enviar a DASN, pois seu desenquadramento foi retroativo a janeiro e portanto você deverá enviar a declaração de ME, a <strong>Defis</strong>.</li>
	<li>Se estiver se desenquadrando por opção, ou por excesso de receita em até 20%, enviará a DASN normalmente em janeiro.</li>
</ul>

