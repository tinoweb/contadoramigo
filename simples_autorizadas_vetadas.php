  <div class="titulo" style="margin-bottom:10px;">Alteração de Contrato Social</div>
  <br />
  Confira a seguir a relação de atividades autorizadas e aquelas expressamente vedadas ao enquadramento no Simples. É importante ressaltar que tratam-se de listas simplificadas, que devem servir apenas como referência. Para a averiguação mais aprofundada das atividades permitidas e vedadas ao Simples Nacional, recomentamos a consulta direta à <a href="http://www.receita.fazenda.gov.br/legislacao/leiscomplementares/2006/leicp123.htm" target="_blank">Lei Complementar nº 123, de 14 de dezembro de 2006</a>.<br />
<br />
<br />
  <span class="tituloVermelho">Atividades autorizadas</span><br />
  <br />
<ul>
<li>creche, pré-escola e estabelecimento de ensino fundamental, escolas técnicas, profissionais e de ensino médio, de línguas estrangeiras, de artes, cursos técnicos de pilotagem, preparatórios para concursos, gerenciais e escolas livres</li>
<br />

<li>agência terceirizada de correios</li>
<br />

<li>agência de viagem e turismo</li>  
<br />

<li>centro de formação de condutores de veículos automotores de transporte terrestre de passageiros e de carga</li>  
<br />

<li>agência lotérica</li>  
<br />

<li>serviços de instalação, de reparos e de manutenção em geral, bem como de usinagem, solda, tratamento e revestimento em metais</li>  
<br />

<li>transporte municipal de passageiros</li>  
<br />

<li>escritórios de serviços contábeis</li> 
<br />

<li>produções cinematográficas, audiovisuais, artísticas e culturais, sua exibição ou apresentação, inclusive no caso de música, literatura, artes cênicas, artes visuais, cinematográficas e audiovisuais.
(obs: como PRODUÇÃO a montagem do palco, preparação da iluminação, venda dos ingressos, manutenção da segurança e provimento de toda a infra-estrutura necessária à realização do evento. Por outro lado, se a ME ou EPP atuar na realização do evento ARTÍSTICO ou cultural propriamente dito, ela NÃO pode ingressar no SIMPLES NACIONAL).</li>
<br /> 

<li>construção de imóveis e obras de engenharia em geral, inclusive sob a forma de subempreitada, execução de projetos e serviços de paisagismo, bem como decoração de interiores;</li>
<br />


<li>serviço de vigilância, limpeza ou conservação. </li>
<br />

<li>cumulativamente administração e locação de imóveis de terceiros; </li>
<br />

<li>academias de dança, de capoeira, de ioga e de artes marciais; </li>
<br />

<li>academias de atividades físicas, desportivas, de natação e escolas de esportes; </li>
<br />

<li>elaboração de programas de computadores, inclusive jogos eletrônicos, desde que desenvolvidos em estabelecimento do optante;</li>  
<br />

<li>licenciamento ou cessão de direito de uso de programas de computação;</li>  
<br />

<li>planejamento, confecção, manutenção e atualização de páginas eletrônicas, desde que realizados em estabelecimento do optante;</li>  
<br />


<li>empresas montadoras de estandes para feiras;  </li>
<br />

<li>produção cultural e artística;  (Revogado a partir de 1° de janeiro de 2010 pela Lei Complementar n° 133, de 28 de dezembro de 2009)</li>
<br />

<li>produção cinematográfica e de artes cênicas;  (Revogado a partir de 1° de janeiro de 2010 pela Lei Complementar n° 133, de 28 de dezembro de 2009)</li>
<br />

<li>laboratórios de análises clínicas ou de patologia clínica;</li>  
<br />

<li>serviços de tomografia, diagnósticos médicos por imagem, registros gráficos e métodos óticos, bem como ressonância magnética;</li> 
<br />

<li>serviços de prótese em geral.</li>
</ul>  

<br />
<strong>Obs: Também poderá optar pelo Simples Nacional a microempresa ou empresa de pequeno porte que se dedique à prestação de outros serviços não elencados acima, desde que não estejam incluídos na lista de atividades vedadas</strong>. <br />
<br />
<br />
<br />
<span class="tituloVermelho">Atividades vedadas</span>
<ul> 
<li>que explore atividade de prestação cumulativa e contínua de serviços de assessoria creditícia, gestão de crédito, seleção e riscos, administração de contas a pagar e a receber, gerenciamento de ativos (asset management), compras de direitos creditórios resultantes de vendas mercantis a prazo ou de prestação de serviços (factoring); </li>
<br />

<li>que tenha sócio domiciliado no exterior; </li>
<br />

<li> de cujo capital participe entidade da administração pública, direta ou indireta, federal, estadual ou municipal;</li> 
<br />

<li> que possua débito com o Instituto Nacional do Seguro Social - INSS, ou com as Fazendas Públicas Federal, Estadual ou Municipal, cuja exigibilidade não esteja suspensa;</li> 
<br />

<li> que preste serviço de transporte intermunicipal e interestadual de passageiros;</li> 
<br />

<li> que seja geradora, transmissora, distribuidora ou comercializadora de energia elétrica; </li>
<br />

<li> que exerça atividade de importação ou fabricação de automóveis e motocicletas;</li>
<br />

<li> que exerça atividade de importação de combustíveis;</li> 
<br />

<li> que exerça atividade de produção ou venda no atacado de:<br />

a) cigarros, cigarrilhas, charutos, filtros para cigarros, armas de fogo, munições e pólvoras, explosivos e detonantes;<br />

b) bebidas alcoólicas; refrigerantes (inclusive águas saborizadas gaseificadas); preparações compostas, não alcoólicas (extratos concentrados ou sabores concentrados), para elaboração de bebida refrigerante, com capacidade de diluição de até 10 (dez) partes da bebida para cada parte do concentrado; cervejas sem álcool;
</li>
<br />

<li> que tenha por finalidade a prestação de serviços decorrentes do exercício de atividade intelectual, de natureza técnica, científica, desportiva, artística ou cultural, que constitua profissão regulamentada ou não, bem como a que preste serviços de instrutor, de corretor, de despachante ou de qualquer tipo de intermediação de negócios;</li> 
<br />

<li> que realize cessão ou locação de mão-de-obra;</li> 
<br />

<li> que realize atividade de consultoria;</li> 
<br />

<li> que se dedique ao loteamento e à incorporação de imóveis.</li> 
<br />

<li> que realize atividade de locação de imóveis próprios, exceto quando se referir a prestação de serviços tributados pelo ISS.
<br />
<br />
</li>
</ul>
