<h2>Inscrição na Junta Comercial de São Paulo</h2>

O <b>Via Rápida Empresa</b> é um site desenvolvido pela <b>Junta Comercial de SP</b> para abertura e Alteração de Empresas. Só pode ser acessado com o navegador <b>Internet Explorer</b>. <br>
<br>
Antes de iniciar qualquer processo, é preciso fazer uma rápida configuração no navegador. Vá em <b>Ferramentas/Configurações do Modo de Exibição de Compatibilidade</b> e adicione o site <b>sp.gov.br</b>, caso contrário alguns campos de preenchimento não funcionarão! Em caso de dúvida durante o preenchimento, consulte nosso <a href="suporte.php">Help Desk</a> ou diretamente o Suporte Técnico Via Rápida Empresa pelos telefones: (11) 3468-3080 / 3468-3083 / 3468-3077  de segunda a sexta das 8h às 17h. <br>
<br>
Você precisará também de um <b>certificado digital</b>. Pode ser E-CPF ou E-CNPJ e não precisa ser seu. <br>
<br>
Se preferir comprar um certificado, como você ainda não tem CNPJ, deverá solicitar um <b>E-CPF.</b> Este certificado  também lhe dá acesso ao Sistema do Imposto de Renda e permite consultar  pendências fiscais. Recomendamos o <a href="http://www.validcertificadora.com.br/e-CPF-A1.htm" target="_blank">modelo E-CPF A1 da Valid Certificadora</a>. <strong>É bom ressaltar que, após a abertura da empresa, você precisará de um Certificado tipo E-CNPJ para cumprir  com suas obrigações fiscais, então se não quiser pagar por 2 certificados, faça a abertura usando o de um amigo</strong>.<br>
