Orientações para o preenchimento da RAIS de anos anteriores:

<ol>
<li>Abra o programa Gerador de Declaração Rais Genérico e no menu principal vá em <strong>Declaração/Nova</strong></li>
<li>Selecione o <strong>Ano-base</strong> e preencha seus dados cadastrais. Ao lado do campo CNPJ, em "prefixo", coloque 00 e em "CEI vinculado", deixe em branco</li>
<li>Em <strong>Natureza Jurídica</strong>, clique na  lupinha ao lado para selecionar o código e a descrição. As microempresas são geralmente Entidades Empresariais do tipo Sociedade Empresária Limitada, Empresário (individual) ou Sociedade Simples. Se estiver na dúvida,  verifique a natureza jurídica em seu comprovante do <a href="http://www.receita.fazenda.gov.br/pessoajuridica/cnpj/cnpjreva/cnpjreva_solicitacao.asp" target="_blank">CNPJ</a></li>
<li> Deixe o campo "Para uso da Empresa" em branco.</li>
<li>Ao terminar de preencher seus dados, salve e feche a janela.</li>
<li>No menu principal, vá em <strong>Informações/Dados do Responsável</strong>. </li>
<li> Preencha os dados de sua empresa como responsável e logo abaixo, em &quot;CPF do Responsável&quot;, &quot;Nome&quot;, &quot;Data de Nascimento&quot; e &quot;E-mail&quot;, coloque os dados do sócio administrador.</li>
<li>No menu principal,  vá em <strong>Declaração/Verificar Insconsistências</strong> e corrija eventuais problemas.</li>
<li> Quando não houver mais insconsistências, vá em <strong>Declaração/Gravar</strong>.</li>
<li> Ao final do processo de gravação, surgirá o botão para transmissão. Clique nele, em seguida marque a opção &quot;transmitir com Certificado Digital&quot; e finalize o processo.</li>
<li> Com a transmissão feita, não se esqueca de imprimir o protocolo.</li>
</ol>
Pronto sua RAIS está transmitida!

