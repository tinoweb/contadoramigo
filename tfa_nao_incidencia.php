<div class="tituloVermelho" style="margin-bottom:20px">Anúncios não sujeitos a taxação</div>

<strong>A Taxa de Fiscalização de Anúncios (TFA) não incide quanto</strong>:
<ul>
  <li>
    Aos anúncios destinados a fins patrióticos e à propaganda de partidos políticos ou de seus candidatos, na forma prevista na legislação eleitoral;
    
  </li><br /><li>
    Aos anúncios no interior de estabelecimentos; divulgando mercadorias, bens, produtos ou serviços neles negociados ou explorados, exceto os de transmissão por via sonora, se audíveis das vias e logradouros públicos; 
    
  </li><br /><li>
    Aos anúncios e emblemas de entidades públicas, ordens e cultos religiosos, irmandades, asilos, orfanatos, entidades sindicais, ordens ou associações profissionais e representações diplomáticas, quando colocados nas respectivas sedes ou dependências; 
    
  </li><br /><li>
    Aos anúncios e emblemas de hospitais, sociedades beneficentes, culturais, esportivas e entidades declaradas de utilidade pública, quando colocados nas respectivas sedes ou dependências; 
    
  </li><br /><li>
    Aos anúncios próprios colocados em instituições de educação; 
    
  </li><br /><li>
    Aos anúncios que contiverem apenas a denominação do prédio; 
    
  </li><br /><li>
    Aos anúncios que indiquem uso, lotação, capacidade ou quaisquer avisos técnicos elucidativos do emprego ou finalidade da coisa, desde que sem qualquer legenda, dístico ou desenho de valor publicitário; 
    
  </li><br /><li>
    Aos anúncios destinados, exclusivamente, à orientação do público, desde que sem qualquer legenda, dístico ou desenho de valor publicitário; 
    
  </li><br /><li>
    Aos anúncios indicativos de oferta de emprego, afixados no estabelecimento do empregador, desde que sem qualquer legenda, dístico ou desenho de valor publicitário;
    
  </li><br /><li>
    Aos anúncios de profissionais liberais, autônomos ou assemelhados, até 0,09m² (nove decímetros quadrados), quando colocados nas respectivas residências e locais de trabalho e contiverem, tão-somente, o nome, a profissão e o número de inscrição do profissional no órgão de classe; 
    
  </li><br /><li>
    Aos anúncios de locação ou venda de imóveis em cartazes ou em impressos de dimensões até 0,09m² (nove decímetros quadrados), quando colocados no respectivo imóvel, pelo proprietário, e sem qualquer legenda, dístico ou desenho de valor publicitário; 
    
  </li><br /><li>
    Aos anúncios em cartazes ou em impressos, com dimensão até 0,09m² (nove decímetros quadrados), quando colocados na própria residência, onde se exerça o trabalho autônomo;
    
  </li><br /><li>
    Aos anúncios afixados por determinação legal, no local da obra de construção civil, durante o período de sua execução, desde que contenham, tão-só, as indicações exigidas e as dimensões recomendadas pela legislação própria; 
    
  </li><br /><li>
    Aos anúncios de afixação obrigatória decorrentes de disposição legal ou regulamentar, sem qualquer legenda, dístico ou desenho de valor publicitário; 
    
  </li><br /><li>
    Aos nomes, siglas, dísticos, logotipos e breves mensagens publicitárias identificativas de empresas que, nas condições legais e regulamentares, se responsabilizem, gratuitamente, pela colocação e manutenção de cestos destinados à coleta de lixo nas vias e logradouros públicos, ou se encarreguem da conservação, sem ônus para a Prefeitura, de parques, jardins, e demais logradouros públicos arborizados, ou, ainda, do plantio e proteção de árvores. 
      
      Observação: nestes casos, a não-incidência da Taxa restringe-se, unicamente, aos nomes, dísticos, logotipos e breves mensagens publicitárias afixadas nos cestos destinados à coleta de lixo, de área não superior a 0,3 m², e em placas ou letreiros, de área igual ou inferior, em sua totalidade, a 0,5 m², afixados nos logradouros cuja conservação esteja permitida à empresa anunciante.
  </li>
</ul><br />

<strong>São isentos de pagamento da Taxa de Fiscalização de Anúncios (TFA):</strong>
<ul>
    <li>
      Utilizados ou explorados pelos participantes da denominada &quot;Feira de Livros&quot;, observados os termos da Lei 11.496/1994. 

    </li><br /><li>
     Utilizados ou explorados nos eventos denominados &quot;Festa do Verde&quot; e &quot;Festa da Primavera&quot;, instituídos pelos Decretos 16.010/1979 e 17.469/1981, veiculados por meio de placas padronizadas, com dimensões e cores estabelecidas pelo Departamento de Parques e Áreas Verdes (Depave), da Secretaria Municipal do Verde e do Meio Ambiente. 
    </li>
  </ul>

