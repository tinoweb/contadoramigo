<div class="tituloVermelho" style="margin-bottom:20px">Orientações para pagamento da TFA de anúncios não cadastrados no seu CCM</div>

Você pode imprimir pela internet a guia para pagamento de anúncios temporários ou não cadastrados no seu CCM. Proceda da seguinte forma: <br />
  <br />
  Encontre nas tabelas abaixo o tipo de anúncio veiculado por sua empresa. Anote o <strong>código</strong>, a <strong>incídência</strong> (anual, mensal ou por evento), a<strong> data de vencimento</strong> e calcule o valor a ser pago, multiplicando o <strong>número de  unidades taxadas</strong> pelo<strong> valor da taxa</strong>.<br />
  <br />
  Acesse a <a href="javascript:abreJanela('https://www3.prefeitura.sp.gov.br/damsp_tfa/App/f002_dados.aspx', 'TFE', 'width=800, height=600, top=150, left=150, scrolling=yes')">página de emissão do DAMSP</a> (Documento de Arrecadação do Municipio de São Paulo)  e preencha os campos indicados. Se a taxa estiver atrasada, os juros e multa serão calculados automaticamente. Em seguida, você poderá imprimir a guia de pagamento.<br />
