<span class="tituloPeq">Observações</span><br /><br />

A Taxa incide uma única vez por período de incidência, independentemente da quantidade de mensagens veiculadas em cada um dos anúncios.<br />
<br />
As alterações referentes ao tipo, características ou tamanho do anúncio, que impliquem  em novo enquadramento nas Tabelas I e II anexas, bem como a transferência do anúncio para  local diverso, geram nova incidência da Taxa. 
