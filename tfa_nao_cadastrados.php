<span class="tituloPeq">Vencimentos</span><br />
<br />

<a name="nota" id="nota"></a><strong class="destaque">Nota 1</strong><br />
    O recolhimento pode ser feito em <strong>até 5 parcelas mensais</strong>, sendo que o <strong>valor de cada uma não poderá ser inferior a  R$ 78,33</strong>.<br />
  No primeiro ano, o vencimento será no dia 10 do segundo mês imediatamente posterior ao do início de veiculação, vencendo-se as demais parcelas a cada dia 10 dos meses imediatamente posteriores. A partir do segundo ano, o vencimento se dará no dia 10 de julho, vencendo-se as demais parcelas a cada dia 10 dos meses imediatamente posteriores.<br />
  <br />


  <strong class="destaque">Nota 2</strong><br />
    O primeiro recolhimento deve ser feito  <strong>até a data de início de veiculação</strong> e nos meses posteriores,  até o dia 10 enquanto durar a veiculação.<br />
<br />

 <strong class="destaque">Nota 3</strong><br />
    O primeiro recolhimento  deverá ser feito<strong> até o último dia útil anterior à veiculação</strong> e nos meses posteriores até o 1º  dia útil, enquanto durar a veiculação.<br />
<br />

  <strong class="destaque">Nota 4</strong><br />
    O recolhimento  deverá ser feito até o<strong> último dia útil anterior à data de início do evento</strong>.<br />
