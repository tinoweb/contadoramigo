<div class="tituloAzul">Cadastro no CEPOM (Fique atento!)</div>
<div>Quando você prestar serviço a um <strong>cliente de outro município </strong>mas sua atividade não for executada na cidade dele, você ainda assim pode sofrer retenção do ISS. Isto porque todo prestador de serviço de outro município<strong> precisa estar previamente cadastrado</strong> na cidade do cliente. Este cadastro é conhecido como CEPOM (Cadastro de Empresas Prestadoras de Outros Municípios). Nele, são solicitadas uma série de informações. Pede-se muitas vezes até a foto da fachada de sua empresa (para provar que  não se trata de uma firma fantasma). Obviamente todo esse processo demora alguns dias e, se você tiver urgência, vai acabar  emitindo a nota sem  cadastro mesmo. Neste caso, pagará o ISS duas vezes: uma para o município do cliente  e outra, embutida na DAS, para o seu próprio município. Você não poderá descontar o ISS na DAS, pois neste caso seu município  - a quem o imposto  é de direito - ficaria sem receber.</div>
