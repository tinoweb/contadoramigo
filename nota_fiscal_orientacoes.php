  <h2>Notas para clientes de outros municípios</h2>
  <div style="margin-bottom:20px">
  Quando sua empresa prestar serviço para outro município  poderá ser necessária a  <strong>retenção do ISS</strong>, ou seja, você emitirá uma nota com o valor ISS  discriminado. Seu cliente então descontará no pagamento a parte referente ao imposto e <strong>fará o recolhimento por você</strong>.<br />
  <br />
  Para que você precise fazer uma nota com retenção, porém, não basta o cliente ser de outro município. Depende  também do<strong> tipo de serviço prestado</strong>. De modo geral, quando o serviço  for <strong>fisicamente executado na outra cidade</strong> (por exemplo  jardinagem, ou a apresentação de um espetáculo) haverá retenção e o ISS será devido ao município onde  foi prestado e não mais ao município sede de sua empresa. Baseado nesse princípio o Governo Federal elaborou em lei uma lista de <a href="javascript:abreDiv('atividades_retencao')">atividades sujeitas a retenção de ISS</a>. <br />
  <br />
  O fato do ISS ser retido pelo seu cliente, porém, não significa que você está pagando mais imposto. É preciso tomar cuidado, pois você já paga o ISS embutido na DAS (a guia do Simples Nacional). Assim sendo, na hora da apuração do Simples, você deve informar o valor total das notas que tiveram retenção, justamente para que o sistema da Receita subtraia o ISS daquelas   que  tiveram o imposto recolhido pelo seu cliente. Seguindo o passo a passo  do Contador Amigo, você naturalmente fará esta separação.

