<h2>Cessão de Mão-de-Obra ou Empreitada</h2>
<div style="margin-bottom:20px">Se a sua empresa faz<strong> serviços de empreitada ou cessão de mão de obra</strong>, pode estar sujeita à retenção do INSS. Há, porém, várias <a href="dispensa_retencao_INSS.php">situações em que a retenção de INSS é dispensada</a>. Se no seu caso for realmente obrigatória, você deverá informar na nota fiscal o valor a ser retido logo abaixo à descrição dos serviços. A retenção é de 3,5% do valor bruto da nota e será recolhida pela empresa que o contratou.<br />
  <br />
  Os custos com materiais ou  equipamentos (exceto os equipamentos manuais) podem ser descontados da base de cálculo. Ou seja: se a nota foi de R$ 10 mil mas você gastou R$ 3 mil com materiais e equipamentos, a retenção do INSS será  de 3,5% sobre 7 mil. Mas isto só vale se o fornecimento desses materiais e equipamentos estiver previsto em contrato e discriminados na nota fiscal. Além disso, a base de cálculo não poderá ser inferior a:
  <ul>
    <li>50% do valor bruto da nota fiscal</li>
    <li>30% do valor bruto da nota fiscal para os <strong>serviços de transporte passageiros</strong>, quando as despesas de combustível e de manutenção dos veículos correrem por sua conta</li>
    <li>65% quando se referir à limpeza hospitalar</li>
    <li>80% quando se referir aos demais tipos de limpezas</li>
  </ul>
Não havendo discriminação de valores em contrato, a base de cálculo da retenção deve corresponder, no mínimo a:
<ul>
  <li>50% para prestação de serviços em geral</li>
  <li>10% para pavimentação asfáltica</li>
  <li>15% para terraplenagem, aterro sanitário e dragagem</li>
  <li>45% para obras de arte (pontes ou viadutos)</li>
  <li>50% para drenagem</li>
  <li>35% para os demais serviços da construção civil realizados com a utilização de equipamentos, exceto os manuais</li>
</ul>
