<H2>DeSTDA</H2>
  
A Declaração de Declaração de Substituição Tributária, Diferencial de Alíquota e Antecipação deverá ser utilizada para declarar o ICMS:<br>
<ul>
<li>devido a título de substituição tributária</li>
<li>devido a título de antecipação do pagamento do imposto</li>
<li>correspondente à diferença entre a alíquota interna e a alíquota interestadual</li>
</ul>

Trata-se de uma declaração mensal <strong>exclusiva das empresas que possuem inscrição estadual</strong>. Normalmente só o comércio tem inscrição estadual, mas algumas empresas de serviços, como transporte intermunicipal, ou radiodifusão, também possuem e estão obrigadas à entrega da D-Stda.<br>
<br>
A normatização relativa ao envio da declaração está a cargo de cada um dos estados. Alguns a dispensaram, outros estão preparando o software e há aqueles em que o sitema já está implantado e a declaração já vem sendo exigida. Por se tratar de uma declaração dispensadada para quase todos os nossos cliente, não dispomos de um tutorial, mas em caso de dúvida com relação eo preenchimento, você pode contatar nosso Help Desk. <br>
<br>
