<br />
</li>
  
    <li>Campo 14: ISS retido na fonte. Só deve ser informado neste campo o ISS descontado por seus clientes ao lhe pagarem pelo serviço. Normalmente não há retenção nenhuma e o valor informado é zero. A retenção ocorre somente para algumas atividades quando o serviço é prestado para <strong>outro município</strong>. Para conferir se você teve retenção, apenas verifique se algum cliente pagou sua nota com desconto. Se foi este o seu caso, informe os ISS retidos separados pelo município do tomador.<br />
      <br />
    </li>

    <li>Campo 15: por serviços de comunicação entenda-se telecomunicações (geração,  emissão, recepção, transmissão, retransmissão, repetição e ampliação).</li>
