    <a class="linkMenu" style="font-size:10px" href="javascript:abreDivFechaOutro('passo8','passo7')">&nbsp;&nbsp;&nbsp;<strong>PRÓXIMO</strong> <img src="images/avancar_azul.png" width="7" height="8" border=0 alt="Avançar" /> </a><br /><br />
	  Clique em <strong>De toda a ME/EPP</strong> e preencha o formulário, conforme as orientações abaixo. Se persistirem dúvidas, <a href="http://www8.receita.fazenda.gov.br/SimplesNacional/Arquivos/manual/MANUAL_PGDAS-D_2018_V4.pdf" target="_blank"> baixe aqui o Manual Oficial</a> (as orientações sobre a Defis começam na página 63)<br />
    
    <br />
    <strong>Seguem algumas dicas de preenchimento:</strong><br />
<ul>
<li>Campo 1: em ganhos de capital, indique o ganho obtido com a venda de bens no ano. Por exemplo, se a empresa comprou um bem por R$ 30 mil e vendeu por R$ 40 mil, deve informar R$ 10 mil. Não se trata de mercadorias à venda em sua loja, mas de bens patrimoniais.<br /><br />
</li>
<li>Campo 4: sua empresa não faz balanço, deixe este campo em branco.<br /><br />
</li>
<li>Campo 7: clique em<strong> inserir novas ocorrências</strong> para adicionar os dados dos outros sócios da sua empresa.<br /><br />
</li>
<li>Campo 7.1: em rendimentos isentos pagos ao sócio, indique o total retirado pelo sócio  como distribuição de lucros. Para saber o valor máximo que você pode declarar, utilize a <a href="distribuicao_de_lucros.php">ferramenta de cálculo de distribuição de lucros</a> do Contador Amigo. Quando houver mais de um sócio, clique em inserir novas ocorrências, para incluí-los. Os campos referentes ao segundo sócio aparecerão logo abaixo do primeiro (talvez seja necessário mover a barra de rolagem para visualizá-lo). <br>
<br>
<strong>Atenção: a soma dos valores distribuídos como lucro entre os sócios não pode ser superior ao total indicado na nossa ferramenta de cálculo de distribuição de lucros</strong>. Em geral, o lucro deve ser rateado proporcionalmente aos percentuais de cada sócio na empresa.<br /><br />
</li>
<li>Campo 7.2: em rendimentos tributáveis pagos ao sócio pela empresa, indique o total bruto retirado pelo sócio como pró-labore. Você pode encotnrar esta informação na folha de pagamento (Atenção: certifique-se de ter cadastrado previamente no Contador Amigo os pró-labores de todos os meses do ano ao qual se refere esta Defis.<br /><br />
</li>
<li>8. O item "Percentual de participação em cotas em tesouraria no capital social da empresa (%)", quer saber se algum sócio deixou a empresa e, ao invés de vender as cotas aos sócios remanescentes, deixou na empresa, ou seja, em "tesouraria".<br>
<br>
</li>
<li>Campo 9: em "total de ganhos <strong>líquidos</strong> auferidos em operações de renda variável", informe o valor correspondente os ganhos líquidos auferidos nas aplicações financeiras de renda 
<strong>fixa</strong> ou <strong>variável</strong> (sim fixa também. É o que consta no manual da Defis, emitido pela Receita Federal).</li>
</ul>
Depois de preencher esta etapa é aconselhável clicar em <strong>Salvar</strong>. Dessa forma se você tiver que terminar a declaração em outra hora não perderá os dados já cadastrados.
